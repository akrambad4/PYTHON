# data types

number = 45 # int
num = 56.78 # float
greeting = "Hello there" # str
is_python_interesting = True # bool

# variable storing multiple values

languages = ["Python", "PhP", "Java", ] # list
fruits = ("apple", "banana", "pineapple") # Tuple
countries = {"Kenya","China","USA"} # set

print(languages)
print(fruits)
print(countries)

# dictionary - multiple values

details = {
    "firstname" : "Allan",
    "age" : 18,
    "course" : "MIT",
    "nationality" : "Kenyan"
}

print(number)
print(greeting)
print(countries)
print(is_python_interesting)
print(details)
print(details["course"])
print(details["nationality"])

# Determining the data type

print(type(details))
print(type(countries))


# Type casting - converting one data type to another

print(float(number))
print(int(num))















