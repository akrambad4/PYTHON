# while loop

# increment
number = 5
while number <= 10:
    print(number)
    number += 1

# Decrement
num = 105
while num >= 100:
    num -= 1
    print(num)

# Break statement
x= 20
while x<= 25:
    print(x)
    if x == 24:
        break
    x += 1

# continue statement - skip
y = 79
while y<= 85:
    y += 1
    if y == 83:
        continue
    print(y)

# for loops
languages = ["Python", "Java", "C++"]
for z in languages:
    print(z)

# range
for mynumber in range(5):
    print(mynumber)

for mynum in range(2,5):
    print(mynum)

for count in range (20,31,2):
    print(count)




